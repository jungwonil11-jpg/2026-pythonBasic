#비교연산자 (결과는 항상 True/false)
#논리연산자 (결과는 항상 True/False)

su1 = 7
su2 = 3

print(f"{su1 > su2 = }")
print(f"{su1 >= su2 = }")
print(f"{su1 < su2 = }")
print(f"{su1 <= su2 = }")
print(f"{su1 == su2 = }")
print(f"{su1 != su2 = }")

# 논리연산자(and, or, not)
print(f"{True and True =}")
print(f"{True and True =}")
print(f"{False and True =}")
print(f"{False and False =}")
print()

print(f"{True or True =}")
print(f"{True or True =}")
print(f"{False or True =}")
print(f"{False or False =}")

age=17
print(f"{bool(age)=}")

print(f"{not bool(age) = }") #False
